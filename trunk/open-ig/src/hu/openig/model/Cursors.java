package hu.openig.model;

/**
 * The enumeration to refer to a cursor for switching.
 * @author danyetz, 2014.07.21.
 */
public enum Cursors {
	/** Blank cursor. */
	BLANK,
	/** Hand cursor. */
	HAND,
	/** Move cursor. */
	MOVE,
	/** Pointer cursor. */
	POINTER,
	/** Redo cursor. */
	REDO,
	/** Select cursor. */
	SELECT,
	/** Target cursor. */
	TARGET
}
