/**
 * Enumeration for the game speed settings.
 * @author karnokd, 2009.05.11.
 * @version $Revision 1.0$
 */
public enum GameSpeed {
	/** Game is paused. */
	PAUSED,
	/** Game runs with normal speed. */
	NORMAL,
	/** Game runs with double speed. */
	FAST,
	/** Game runs with quad speed. */
	ULTRAFAST;
}
